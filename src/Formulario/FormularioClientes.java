    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
 String cedula = txtCedula.getText();
    String nombre = txtNombre.getText();
    String apellido = txtApellido.getText();
    String direccion = txtDireccion.getText();
    String telefono = txtTelefono.getText();
    String Correo = txtCorreo.getText();

    if (cedula.isEmpty() || nombre.isEmpty() || apellido.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "CÃ©dula, Nombre y Apellido son obligatorios.");
        return;
    }

    Clases.ClientesDAO.guardarClientes(cedula, nombre, apellido, direccion, telefono, Correo);
    javax.swing.JOptionPane.showMessageDialog(this, "Cliente guardado correctamente.");

    txtCedula.setText("");
    txtNombre.setText("");
    txtApellido.setText("");
    txtDireccion.setText("");
    txtTelefono.setText("");
    txtCorreo.setText("");
    
    }//GEN-LAST:event_btnGuardarActionPerformed

